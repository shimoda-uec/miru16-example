"""
This is a sample math package for "Wakate-no-kai" (it means the working group
of young researchers) at MIRU 2016 (Meeting of Image Recoginition and
Understanding, a domestic conference in Japan).
"""

def add(x, y):
    """
    Add two numbers.
    """
    return x + y

def sub(x, y):
    """
    Subtract y from x.
    """
    return x - y

def mul(x, y):
    """
    Multiply x and y.
    """
    return x * y

def div(x, y):
    """
    Divide x with y.
    """
    return x / y
